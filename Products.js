function searchProducts(products, searchInput) {
    var allResults= {};
    const searchTerms = searchInput.split(" ");
    for (var product in products) {
        var matchCounter = 0;
        for (var searchTerm in searchTerms) {
            for (var tag in ((products[product])["tags"])) {
                if ((((products[product])["tags"])[tag]).includes(searchTerms[searchTerm].toLowerCase())) {
					matchCounter += 1;
                } else if (searchTerms[searchTerm].toLowerCase().includes((((products[product])["tags"])[tag]))) {
					matchCounter += 1;
                }
            }
        }
        if (matchCounter != 0) {
            allResults[product] = matchCounter;
        }
    }
	var sortedResults = Object.keys(allResults).map(
		(result) => { return [result, allResults[result]] });
	sortedResults.sort(
		(first, second) => { return second[1] - first[1] }
	);
    return sortedResults;
}

function searchCollectionsFromResults(sortedResults) {
	var allCollectionResults = {};
	for (var product in sortedResults) {
		var collectionName = (products[sortedResults[product][0]])["collection"];
		if (collectionName !== undefined) {
			if (!(collectionName in allCollectionResults)) {
				allCollectionResults[collectionName] = 1;
			} else {
				allCollectionResults[collectionName] += 1;
			}
		}
	}
	for (var collection in allCollectionResults) {
		if (allCollectionResults[collection] < 2) {
			delete allCollectionResults[collection];
		}
	}
	var sortedCollectionResults = Object.keys(allCollectionResults).map(
		(result) => { return [result, allCollectionResults[result]] });
	sortedCollectionResults.sort(
		(first, second) => { return second[1] - first[1] }
	);
    return sortedCollectionResults;
}

